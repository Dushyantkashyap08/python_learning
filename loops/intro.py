 #loops : used to iterate over a sequence of elements
 #two types of loops : for loop and while loop
 
#for-loop
 
print("for loop example")
for i in range(1,6): #in range we can define startIndex and stopIndex. startIndex is optional and by default it is 0. the stopIndex will be excluded from the operation in loop
    print(i)
    
    
#while -loop
print("while loop example")
i = 1
while (i <= 5): #loop will continue to execute untill the condition becomes false
    print(i)
    i = i + 1